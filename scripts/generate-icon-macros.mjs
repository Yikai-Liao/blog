import fs from "node:fs/promises";
import { createRequire } from "node:module";
import path from "node:path";

const require = createRequire(import.meta.url);

const iconNames = [
	"material-symbols:home-outline-rounded",
	"material-symbols:search",
	"material-symbols:palette-outline",
	"material-symbols:dark-mode-outline-rounded",
	"material-symbols:menu-rounded",
	"fa6-brands:github",
	"fa6-brands:debian",
	"fa6-solid:arrow-up-right-from-square",
	"material-symbols:chevron-right-rounded",
	"material-symbols:chevron-left-rounded",
	"material-symbols:keyboard-arrow-up-rounded",
	"fa6-solid:arrow-rotate-left",
	"material-symbols:lock-outline",
	"material-symbols:notes-rounded",
	"material-symbols:schedule-outline-rounded",
	"material-symbols:calendar-today-outline-rounded",
	"material-symbols:book-2-outline-rounded",
	"material-symbols:tag-rounded",
	"fa6-brands:creative-commons",
];

const collectionCache = new Map();

function splitIconName(fullName) {
	const separator = fullName.indexOf(":");
	if (separator < 1 || separator === fullName.length - 1) {
		throw new Error(`Icon name must be collection:name, got: ${fullName}`);
	}
	return [fullName.slice(0, separator), fullName.slice(separator + 1)];
}

function loadIcon(fullName) {
	const [collection, name] = splitIconName(fullName);
	if (!collectionCache.has(collection)) {
		collectionCache.set(
			collection,
			require(`@iconify-json/${collection}/icons.json`),
		);
	}
	const data = collectionCache.get(collection);
	const icon = data.icons[name];
	if (!icon) throw new Error(`Missing Iconify icon: ${fullName}`);
	return {
		...icon,
		width: icon.width || data.width || 24,
		height: icon.height || data.height || 24,
	};
}

function renderSvg(fullName) {
	const icon = loadIcon(fullName);
	return `<svg viewBox="0 0 ${icon.width} ${icon.height}" aria-hidden="true">${icon.body}</svg>`;
}

const lines = [
	"{# Generated by scripts/generate-icon-macros.mjs. Do not edit by hand. #}",
	"{% macro icon(name) %}",
];

iconNames.forEach((name, index) => {
	lines.push(`  {% ${index === 0 ? "if" : "elif"} name == "${name}" %}`);
	lines.push(`    ${renderSvg(name)}`);
});
lines.push("  {% endif %}");
lines.push("{% endmacro icon %}");
lines.push("");

await fs.mkdir(path.join("templates", "generated"), { recursive: true });
await fs.writeFile(
	path.join("templates", "generated", "icons.html"),
	`${lines.join("\n")}\n`,
);
